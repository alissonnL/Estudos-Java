    private void btnLiberarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLiberarActionPerformed

        try {
            registro.setDataSaida(LocalDate.parse(ftxtDataSaida.getText(), dtf));
            if (registro.getDataSaida().isBefore(registro.getDataEntrada())) {
                JOptionPane.showMessageDialog(null, "Digite uma data válida", "Data inválida", JOptionPane.WARNING_MESSAGE);
                ftxtDataSaida.requestFocus();
            } else {
                lblValorHospedagem.setText(String.valueOf(registro.liberarQuarto()));
                daoQuarto.alterar(registro.getQuarto());
                btnLiberar.setEnabled(false);
                ftxtDataSaida.setEnabled(false);
                if (registro.getQuarto().getSituacao() == false) {
                    lblSituacao.setText("Livre");
                } else {
                    lblSituacao.setText("Ocupado");
                }

                daoRegistro.alterar(registro);

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Digite uma data válida", "Data inválida", JOptionPane.WARNING_MESSAGE);
            ftxtDataSaida.requestFocus();
        }
    }//GEN-LAST:event_btnLiberarActionPerformed

    private void btnReservarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReservarActionPerformed

        
        try {
            LocalDate data = LocalDate.parse(ftxtDataEntrada.getText(), dtf);
            registro = new Registro(Integer.parseInt(txtCodigo.getText()), data, recepcionista);
            registro.reservarQuarto(hospede, quarto);
            daoQuarto.alterar(registro.getQuarto());
            daoRegistro.inserir(registro);

            btnReservar.setEnabled(false);
            btnLiberar.setEnabled(false);
            ftxtDataEntrada.setEnabled(false);
            ftxtDataEntrada.setText("");
            btnConsultar.setEnabled(true);

            txtRegFunc.setText("");
            ftxtCPF.setText("");
            txtNumQuarto.setText("");
            txtCodigo.setText("");
            ftxtDataSaida.setText("");
            lblValorHospedagem.setText("");
            lblRecepcionista.setText("");
            lblHospede.setText("");
            lblSituacao.setText("");

            txtCodigo.setEnabled(true);
            txtCodigo.requestFocus();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data de Entrada inválida", "Data inválida", JOptionPane.WARNING_MESSAGE);
            ftxtDataEntrada.requestFocus();
        }
    }//GEN-LAST:event_btnReservarActionPerformed

