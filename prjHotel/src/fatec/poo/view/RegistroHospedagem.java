    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        registro = null;

        try {
            registro = daoRegistro.consultar(Integer.parseInt(txtCodigo.getText()));

            if (registro == null) {
                txtRegFunc.setEnabled(true);
                btnConsultar.setEnabled(false);
                txtCodigo.setEnabled(false);
                btnReservar.setEnabled(true);

                txtRegFunc.requestFocus();
                btnSelRecepcionista.setEnabled(true);

            } else {

                if (registro.getDataSaida() != null) {
                    ftxtDataSaida.setText(registro.getDataSaida().format(dtf));
                    lblValorHospedagem.setText(String.valueOf(registro.getValorHospedagem()));
                } else {
                    btnLiberar.setEnabled(true);
                    ftxtDataSaida.setEnabled(true);
                    txtCodigo.setEnabled(false);
                    ftxtDataSaida.requestFocus();

                }

                txtRegFunc.setText(String.valueOf(registro.getRecepcionista().getRegFunc()));
                ftxtCPF.setText(registro.getHospede().getCpf());
                txtNumQuarto.setText(String.valueOf(registro.getQuarto().getNumero()));

                ftxtDataEntrada.setText(registro.getDataEntrada().format(dtf));
                lblRecepcionista.setText(registro.getRecepcionista().getNome());
                lblHospede.setText(registro.getHospede().getNome());
                lblRecepcionista.setText(registro.getRecepcionista().getNome());

                if (!registro.getQuarto().getSituacao()) {
                    lblSituacao.setText("Livre");
                } else {
                    lblSituacao.setText("Ocupado");
                }

                btnConsultar.setEnabled(false);

            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Digite um codigo valido", "Dados inválidos", JOptionPane.WARNING_MESSAGE);
            txtCodigo.requestFocus();
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

